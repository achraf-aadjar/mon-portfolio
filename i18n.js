// Active language
let currentLang = 'fr';

function setLang(lang) {
  currentLang = lang;
  const t = translations[lang];
  if (!t) return;

  // Apply translations
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = t[key];
      } else {
        el.innerHTML = t[key].replace(/\n/g, '<br>');
      }
    }
  });

  // RTL for Arabic
  if (lang === 'ar') {
    document.documentElement.setAttribute('lang', 'ar');
    document.documentElement.setAttribute('dir', 'rtl');
    document.body.classList.add('rtl');
    document.body.style.fontFamily = "'Noto Kufi Arabic', sans-serif";
  } else {
    document.documentElement.setAttribute('lang', lang);
    document.documentElement.setAttribute('dir', 'ltr');
    document.body.classList.remove('rtl');
    document.body.style.fontFamily = "'Space Grotesk', sans-serif";
  }

  // Update typed text
  if (window.updateTyped) window.updateTyped(t["hero.typed"] || []);

  // Update active button
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });

  // Save preference
  localStorage.setItem('lang', lang);
}

function getCurrentTranslation(key) {
  return (translations[currentLang] || translations['fr'])[key];
}
